#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!del)
		return ;
	if (lst)
	{
		(*del)(lst->content);
		free(lst);
	}
}
//ligne 21 (*del) adresse de la fonction qui permet de supprimer le
//contennu de l'element
